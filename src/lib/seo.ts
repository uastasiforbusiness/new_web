/**
 * Brand social profiles for JSON-LD sameAs.
 * Prefer comma-separated NEXT_PUBLIC_SAME_AS, or individual NEXT_PUBLIC_SOCIAL_* vars.
 * Empty entries are filtered — never emit placeholder URLs.
 */
export function getSameAs(): string[] {
  const fromList =
    process.env.NEXT_PUBLIC_SAME_AS?.split(",")
      .map((s) => s.trim())
      .filter((s) => s.startsWith("http")) ?? [];

  const individuals = [
    process.env.NEXT_PUBLIC_SOCIAL_INSTAGRAM,
    process.env.NEXT_PUBLIC_SOCIAL_FACEBOOK,
    process.env.NEXT_PUBLIC_SOCIAL_X,
    process.env.NEXT_PUBLIC_SOCIAL_LINKEDIN,
    process.env.NEXT_PUBLIC_SOCIAL_YOUTUBE,
    process.env.NEXT_PUBLIC_SOCIAL_TRIPADVISOR,
  ]
    .map((s) => s?.trim())
    .filter((s): s is string => !!s && s.startsWith("http"));

  return Array.from(new Set([...fromList, ...individuals]));
}

export type PageMeta = {
  title: string;
  description: string;
  path: string;
  keywords?: string[];
  ogImage?: string;
  noIndex?: boolean;
};

/**
 * Consistent page metadata for App Router.
 * Title suffix "| B LEADER" is applied for non-home paths.
 */
export function buildPageMeta(page: PageMeta) {
  const title =
    page.path === "/" ? page.title : `${page.title} | ${SITE.name}`;
  const description = page.description || SITE.defaultDescription;
  const canonicalPath = page.path === "/" ? "/" : page.path;
  const absoluteUrl = `${SITE.url}${canonicalPath === "/" ? "" : canonicalPath}`;
  const images = page.ogImage
    ? [{ url: page.ogImage, width: 1200, height: 630, alt: title }]
    : [{ url: SITE.ogImage, width: 1200, height: 630, alt: title }];

  return {
    title,
    description,
    keywords: [...SITE.defaultKeywords, ...(page.keywords ?? [])],
    metadataBase: new URL(SITE.url),
    alternates: {
      canonical: canonicalPath,
      languages: {
        "en-US": canonicalPath,
        "x-default": canonicalPath,
      },
    },
    openGraph: {
      title,
      description,
      url: absoluteUrl,
      siteName: SITE.name,
      type: "website" as const,
      locale: SITE.locale,
      images,
    },
    twitter: {
      card: "summary_large_image" as const,
      title,
      description,
      images: images.map((i) => i.url),
      ...(SITE.twitterHandle
        ? { creator: SITE.twitterHandle, site: SITE.twitterHandle }
        : {}),
    },
    robots: page.noIndex
      ? { index: false, follow: false }
      : {
          index: true,
          follow: true,
          googleBot: {
            index: true,
            follow: true,
            "max-video-preview": -1,
            "max-image-preview": "large" as const,
            "max-snippet": -1,
          },
        },
  };
}


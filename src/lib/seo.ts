/** WebSite schema — helps sitelinks / brand search understanding */
export function websiteSchema() {
  return {
    "@context": "https://schema.org",
    "@type": "WebSite",
    "@id": `${SITE.url}/#website`,
    name: SITE.name,
    url: SITE.url,
    description: SITE.defaultDescription,
    inLanguage: "en-US",
    publisher: { "@id": `${SITE.url}/#organization` },
  };
}

export type Vehicle = {
  name: string;
  image: string;
  pricePerDay?: number;
  currency?: string;
  description: string;
  brand: string;
  category: "Car" | "Yacht";
};

/**
 * Product schema for rental vehicles.
 * UnitPriceSpecification = daily rental (not sale).
 */
export function productSchema(vehicle: Vehicle) {
  return {
    "@context": "https://schema.org",
    "@type": "Product",
    name: vehicle.name,
    brand: { "@type": "Brand", name: vehicle.brand },
    category: `${vehicle.category} Rental`,
    image: vehicle.image,
    description: vehicle.description,
    offers: {
      "@type": "Offer",
      priceCurrency: vehicle.currency ?? "EUR",
      price: vehicle.pricePerDay ?? undefined,
      priceSpecification: vehicle.pricePerDay
        ? {
            "@type": "UnitPriceSpecification",
            unitText: "DAY",
            price: vehicle.pricePerDay,
            priceCurrency: vehicle.currency ?? "EUR",
          }
        : undefined,
      availability: "https://schema.org/InStock",
      url: vehicle.category === "Yacht" ? `${SITE.url}/yacht` : `${SITE.url}/fleet`,
      seller: { "@id": `${SITE.url}/#organization` },
    },
  };
}

/** BreadcrumbList — site hierarchy for SERP */
export function breadcrumbSchema(items: { name: string; path: string }[]) {
  return {
    "@context": "https://schema.org",
    "@type": "BreadcrumbList",
    itemListElement: items.map((item, i) => ({
      "@type": "ListItem",
      position: i + 1,
      name: item.name,
      item: `${SITE.url}${item.path === "/" ? "" : item.path}`,
    })),
  };
}

/** FAQPage schema — rich results for US-intent landing FAQs */
export function faqSchema(faqs: { question: string; answer: string }[]) {
  return {
    "@context": "https://schema.org",
    "@type": "FAQPage",
    mainEntity: faqs.map((faq) => ({
      "@type": "Question",
      name: faq.question,
      acceptedAnswer: {
        "@type": "Answer",
        text: faq.answer,
      },
    })),
  };
}

/** TouristTrip / Service schema for experience landing pages */
